'use client'

import Link from 'next/link'
import { Shield, Activity, Star, AlertTriangle, ArrowRight } from 'lucide-react'
import { ErrorBanner } from '@/components/admin/ErrorBanner'
import { useAdminFetch } from '@/hooks/admin/useAdminFetch'
import { StatsGrid } from '@/components/admin/dashboard/StatsGrid'
import { ActivityChart } from '@/components/admin/dashboard/ActivityChart'
import { RecentActivity } from '@/components/admin/dashboard/RecentActivity'
import { PendingReports } from '@/components/admin/dashboard/PendingReports'

interface StatsResponse {
  stats: {
    totalUsers: number
    totalArtisans: number
    totalBookings: number
    totalRevenue: number
    pendingReports: number
    averageRating: number
    newUsersToday: number
    newBookingsToday: number
    activeUsers7d: number
    trends: {
      users: number
      bookings: number
      revenue: number
    }
  }
  recentActivity: Array<{
    id: string
    type: 'booking' | 'review' | 'report' | 'user'
    action: string
    details: string
    timestamp: string
    status?: string
  }>
  pendingReports: Array<{
    id: string
    target_type: string
    reason: string
    description: string | null
    status: string
    created_at: string
    reporter_id: string | null
  }>
  chartData: Array<{
    date: string
    bookings: number
    users: number
    reviews: number
  }>
}

export default function AdminDashboard() {
  const { data, isLoading, error, mutate } = useAdminFetch<StatsResponse>('/api/admin/stats')

  return (
    <div className="min-h-screen bg-gray-50" aria-label="Tableau de bord administration">
      {/* Header */}
      <header className="bg-white border-b border-gray-200">
        <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8">
          <div className="flex items-center justify-between h-16">
            <div className="flex items-center gap-3">
              <Shield className="w-8 h-8 text-blue-600" />
              <div>
                <h1 className="text-xl font-bold text-gray-900">Tableau de bord</h1>
                <p className="text-sm text-gray-500">Vue d&apos;ensemble de la plateforme</p>
              </div>
            </div>
            <div className="flex items-center gap-4">
              {data?.stats && data.stats.pendingReports > 0 && (
                <div className="flex items-center gap-2 px-3 py-1.5 bg-red-100 text-red-700 rounded-full text-sm font-medium">
                  <AlertTriangle className="w-4 h-4" />
                  {data.stats.pendingReports} signalement{data.stats.pendingReports > 1 ? 's' : ''}
                </div>
              )}
            </div>
          </div>
        </div>
      </header>

      <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 py-8 space-y-8">
        {error && (
          <ErrorBanner
            message={error.message}
            onDismiss={() => mutate()}
            onRetry={() => mutate()}
          />
        )}

        {/* Stats Grid with trends */}
        <StatsGrid stats={data?.stats ?? null} loading={isLoading} />

        {/* Quick metrics row */}
        <div className="grid grid-cols-1 md:grid-cols-3 gap-6">
          {/* Today */}
          <div className="bg-white rounded-xl p-6 shadow-sm border border-gray-100">
            <div className="flex items-center gap-3 mb-4">
              <Activity className="w-5 h-5 text-blue-600" />
              <h3 className="font-semibold text-gray-900">Aujourd&apos;hui</h3>
            </div>
            {isLoading ? (
              <div className="space-y-3 animate-pulse">
                <div className="h-4 bg-gray-200 rounded w-full" />
                <div className="h-4 bg-gray-200 rounded w-3/4" />
                <div className="h-4 bg-gray-200 rounded w-5/6" />
              </div>
            ) : (
              <div className="space-y-3">
                <div className="flex justify-between">
                  <span className="text-gray-600">Nouveaux utilisateurs</span>
                  <span className="font-semibold">{data?.stats?.newUsersToday ?? 0}</span>
                </div>
                <div className="flex justify-between">
                  <span className="text-gray-600">Nouvelles réservations</span>
                  <span className="font-semibold">{data?.stats?.newBookingsToday ?? 0}</span>
                </div>
                <div className="flex justify-between">
                  <span className="text-gray-600">Utilisateurs actifs (7j)</span>
                  <span className="font-semibold">{data?.stats?.activeUsers7d ?? 0}</span>
                </div>
              </div>
            )}
          </div>

          {/* Quality */}
          <div className="bg-white rounded-xl p-6 shadow-sm border border-gray-100">
            <div className="flex items-center gap-3 mb-4">
              <Star className="w-5 h-5 text-amber-500" />
              <h3 className="font-semibold text-gray-900">Qualité</h3>
            </div>
            {isLoading ? (
              <div className="flex justify-center py-4 animate-pulse">
                <div className="w-16 h-10 bg-gray-200 rounded" />
              </div>
            ) : (
              <div className="text-center py-2">
                <p className="text-4xl font-bold text-gray-900">
                  {data?.stats?.averageRating ?? 0}
                </p>
                <p className="text-sm text-gray-500 mt-1">Note moyenne</p>
                <div className="flex justify-center mt-2">
                  {[1, 2, 3, 4, 5].map((star) => (
                    <Star
                      key={star}
                      className={`w-5 h-5 ${
                        star <= Math.round(data?.stats?.averageRating ?? 0)
                          ? 'fill-amber-400 text-amber-400'
                          : 'text-gray-300'
                      }`}
                    />
                  ))}
                </div>
              </div>
            )}
          </div>

          {/* Moderation */}
          <div className="bg-white rounded-xl p-6 shadow-sm border border-gray-100">
            <div className="flex items-center gap-3 mb-4">
              <AlertTriangle className="w-5 h-5 text-red-500" />
              <h3 className="font-semibold text-gray-900">Modération</h3>
            </div>
            {isLoading ? (
              <div className="space-y-3 animate-pulse">
                <div className="h-4 bg-gray-200 rounded w-full" />
                <div className="h-10 bg-gray-200 rounded w-full mt-2" />
              </div>
            ) : (
              <div className="space-y-4">
                <div className="flex justify-between items-center">
                  <span className="text-gray-600">Signalements en attente</span>
                  <span
                    className={`px-2 py-1 rounded-full text-sm font-medium ${
                      (data?.stats?.pendingReports ?? 0) > 0
                        ? 'bg-red-100 text-red-700'
                        : 'bg-green-100 text-green-700'
                    }`}
                  >
                    {data?.stats?.pendingReports ?? 0}
                  </span>
                </div>
                <Link
                  href="/admin/signalements"
                  className="flex items-center justify-center gap-2 w-full px-4 py-2 bg-gray-100 text-gray-700 rounded-lg hover:bg-gray-200 transition-colors text-sm"
                >
                  Voir les signalements
                  <ArrowRight className="w-3.5 h-3.5" />
                </Link>
              </div>
            )}
          </div>
        </div>

        {/* Activity Chart */}
        <ActivityChart data={data?.chartData ?? []} loading={isLoading} />

        {/* Two columns: Recent Activity + Pending Reports */}
        <div className="grid grid-cols-1 lg:grid-cols-5 gap-8">
          <div className="lg:col-span-3">
            <RecentActivity activity={data?.recentActivity ?? []} loading={isLoading} />
          </div>
          <div className="lg:col-span-2">
            <PendingReports
              reports={data?.pendingReports ?? []}
              loading={isLoading}
              onMutate={() => mutate()}
            />
          </div>
        </div>
      </div>
    </div>
  )
}
