/**
 * Client Demandes (Quote Requests) API
 * GET: Fetch quote requests submitted by the client
 */

import { NextResponse } from 'next/server'
import { createClient } from '@/lib/supabase/server'
import { logger } from '@/lib/logger'

export const dynamic = 'force-dynamic'

export async function GET() {
  try {
    const supabase = await createClient()

    // Get current user
    const { data: { user }, error: authError } = await supabase.auth.getUser()

    if (authError || !user) {
      return NextResponse.json(
        { error: 'Non authentifié' },
        { status: 401 }
      )
    }

    // Fetch devis requests for this client
    const { data: demandes, error: demandesError } = await supabase
      .from('devis_requests')
      .select(`
        *,
        devis:devis(
          *,
          artisan:profiles!artisan_id(id, full_name, company_name, avatar_url)
        )
      `)
      .eq('client_id', user.id)
      .order('created_at', { ascending: false })

    if (demandesError) {
      logger.error('Error fetching demandes:', demandesError)
      return NextResponse.json(
        { error: 'Erreur lors de la récupération des demandes' },
        { status: 500 }
      )
    }

    // Calculate stats
    const stats = {
      total: demandes?.length || 0,
      enAttente: demandes?.filter(d => d.status === 'pending').length || 0,
      devisRecus: demandes?.filter(d => d.status === 'sent').length || 0,
      acceptes: demandes?.filter(d => d.status === 'accepted').length || 0,
      termines: demandes?.filter(d => d.status === 'completed').length || 0,
    }

    return NextResponse.json({
      demandes: demandes || [],
      stats
    })
  } catch (error) {
    logger.error('Client demandes GET error:', error)
    return NextResponse.json(
      { error: 'Erreur serveur' },
      { status: 500 }
    )
  }
}
